#include <iostream>
#include <vector>

using namespace std;

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    
    int t; cin >> t;
    while (t--){
        int n,m; cin >> n >> m;
        for (int i = 0; i < m; i++){
            int u,v; cin >> u >> v;
        }
        cout << n-1 << '\n';
    }


    return 0;
}